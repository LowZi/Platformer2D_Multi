
package ch.hearc.p2.game.network;

public class IndividualScore {

    /*------------------------------------------------------------------*\
    |*			Attributs Private				*|
    \*------------------------------------------------------------------*/

    private int kill;
    private int death;

    /*------------------------------------------------------------------*\
    |*			Constructeurs					*|
    \*------------------------------------------------------------------*/

    public IndividualScore() {
	this.kill = 0;
	this.death = 0;
    }

    /*------------------------------------------------------------------*\
    |*			Methodes Public					*|
    \*------------------------------------------------------------------*/

    public void addKill() {
	this.kill++;
    }

    public void addDeath() {
	this.death++;
    }

    /*------------------------------*\
    |*		Set		    *|
    \*------------------------------*/

    /*------------------------------*\
    |*		Get		    *|
    \*------------------------------*/

    public int getKill() {
	return this.kill;
    }

    public int getDeath() {
	return this.death;
    }

    /*------------------------------------------------------------------*\
    |*			Methodes Private				*|
    \*------------------------------------------------------------------*/
}
